import Foundation


//open, piblic, fileprivate, private, internal(default)

class MathUtility {
    
    func add(a: Int, b: Int) -> Int {
        return a + b
    }
    func sub(a: Int, b: Int) -> Int {
        return a - b
    }
    func multiply(a: Int, b: Int) -> Int {
        return a * b
    }
    func divide(a: Int, b: Int) -> Int {
        return a / b
    }
}
    
    
